    [Serializable()]
    public class SubActivity : DomainBaseMetaInfoStatus, IEduPathItem
    {
        public virtual String Name { get; set; }
        public virtual String Description { get; set; }
        //public virtual Int64 MinCompletion { get; set; }
        public virtual Int16 DisplayOrder { get; set; }
        public virtual SubActivityType ContentType { get; set; }
        public virtual String Link { get; set; }
        public virtual Activity ParentActivity { get; set; }
        public virtual long IdObjectLong { get; set; }
        public virtual long IdObjectVersion { get; set; }
        public virtual Guid IdObjectGuid { get; set; }
        public virtual int IdModuleAction { get; set; }
        public virtual int IdModule { get; set; }
        public virtual String CodeModule { get; set; }
        public virtual liteCommunity Community { get; set; }
        public virtual long ContentPermission { get; set; }
        public virtual ModuleLink ModuleLink { get; set; }
